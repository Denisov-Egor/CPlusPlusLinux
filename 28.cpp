#include <iostream>

using namespace std;

int main()
{
  int a;

  cin >> a;

  switch (a)
  {
  case 1:
    cout << "1";
    break;
  case 2:
    cout << "2";
    break;
  case 3:
    cout << "3";
    break;
  case 4:
    cout << "4";
    break;
  case 5:
    cout << "5";
    break;
  default:
  cout << "0";
    break;
  }
}